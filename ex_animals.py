#In loop writing about three different animals who have common charecterstics		
animals = ['tiger', 'lion', 'jaguar', 'leopard',  'snow leopard']
for animal in animals:
	print(f"The term big cat is typically used to refer to {animal.title()}.")
	print(f"{animal.title()} is carnivores and efficient apex predator.\n")
print("Despite enormous differences in size, their species are quite similar in both structure and behaviour.")